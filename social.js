function enlargeImg() {
   var element = document.getElementById("TableOne");
   element.classList.toggle("largetable");
   
   var x = document.getElementById("Row1");
  if (x.style.display === "none") {
    x.style.display = "block";
  } else {
    x.style.display = "none";
  }
}

