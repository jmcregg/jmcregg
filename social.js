function enlargeImg() {
   var element = document.getElementById("myImg");
   element.classList.toggle("imgTwo");
 
   var x = document.getElementById("captionOne");
  if (x.style.display === "none") {
    x.style.display = "inline";
  } else {
    x.style.display = "none";
  }
}

